// Java program to find index of a word

package com.strings;

// Main class
public class Main {

    // Main method to run java program
    public static void main(String[] args) {

        // Creating string to practice
        String str = "Hello planet Earth. You are great planet.";

        // Finding index of word 'great' and printing to console
        System.out.println(str.indexOf("great")); // Output: 28
    }
}